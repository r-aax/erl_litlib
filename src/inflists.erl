% Types export.
-export_type([inflist/0]).

% Define infinite list as a record.
-record(inflist,
{
	h :: term(),
	acc :: term(),
	f :: fun((term(), term()) -> {term(), term()})
}).

% Infinite list.
-type inflist() :: #inflist{}.

